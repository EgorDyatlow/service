def schedule_tests
  puts "Тестируем расписание задач"
  tasks=[
    {"Время":"09:00","Задача":"Встреча с клиентом"},
    {"Время":"11:00","Задача":"Настройка оборудования"},
    {"Время":"14:00","Задача":"Выезд к клиенту"}
  ]
  if tasks.is_a?(Array) && !tasks.empty?
    puts "Список задач успешно загружен. Количество:#{tasks.length}"
    tasks.each do |task_item|
      puts "Время:#{task_item['Время']},Задача:#{task_item['Задача']}"
    end
    puts "Проверка: Все задачи имеют время и описание."
    if tasks.all? {|t| t.key?('Время') && t.key?('Задача')}
      puts "Все задачи имеют необходимые поля."
    else
      puts "ОШИБКА: Некоторые задачи не имеют необходимых полей."
    end
  else
    puts "ОШИБКА:Список задач пуст или имеет неверный формат."
  end
  puts "Тесты расписания завершены"
end
puts "Запуск тестов из ветки"
schedule_tests
puts "Все тесты завершены."