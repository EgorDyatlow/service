def client_tests
  puts "Тестируем список клиентов"
  customers=[
    {"имя": "ООО «ТехСервис»","телефон": "+79001234567"},
    {"имя": "ИП Смирнова","телефон": "+79007654321"}
  ]
  if customers.is_a?(Array) && !customers.empty?
    puts "Список клиентов успешно загружен. Количество: #{customers.length}"
    customers.each do |customer|
      puts "Имя:#{customer['имя']},Телефон:#{customer['телефон']}"
    end
    puts "Проверка: Все клиенты имеют имя и телефон."
    if customers.all? {|c| c.key?('имя') && c.key?('телефон')}
      puts "Проверка пройдена:Все клиенты имеют необходимые поля."
    else
      puts "ОШИБКА:Некоторые клиенты не имеют необходимых полей."
    end
  else
    puts "ОШИБКА:Список клиентов пуст или имеет неверный формат."
  end
  puts "Тесты клиентов завершены"
end
